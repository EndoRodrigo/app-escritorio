    private void validar() {
        var user = this.textUser.getText();
        var pass = this.txtPss.getText();

        if ("user".equals(user) && "admin".equals(pass)) {
            mostrarMensaje("Datos correctos! Bienvenido");
        } else {
            mostrarMensaje("Datos incorectos! Invente de nuevo");
        }
    }

